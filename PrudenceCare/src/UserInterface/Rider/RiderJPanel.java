    public void populatePatientRequests(){
                      DefaultTableModel model = (DefaultTableModel)medicineTable.getModel();
        model.setRowCount(0);
        List<MedicineWorkRequest> requestList = account.getPharmaWorkQueue().getDispensaryList();
        for(MedicineWorkRequest req: requestList){
            String medList = "";
                Map<Medicine,Integer> medMap= req.getMedList();
                for (Map.Entry<Medicine,Integer> medicine : medMap.entrySet()) {  
                    if(medList.equals("")){
                        medList+=medicine.getKey();
                    }else{    
                        medList+=","+medicine.getKey();
                  }
                } 
             Object row[] = new Object[8];
             row[0] = req;
                 row[1] = req.getReceiver();
                 row[2] = medList;
                 row[3] = req.getSender();
                 row[4] = req.getMessage();
                 model.addRow(row); 
            }
    }

    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        // TODO add your handling code here:
                int selectedRow = medicineTable.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a Request from table!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        MedicineWorkRequest req= (MedicineWorkRequest)medicineTable.getValueAt(selectedRow, 0);
        
        UserAccount ua = req.getReceiver();
        req.setMessage(txtMsg.getText());
        ua.getPharmaWorkQueue().updateDispensaryRequest(req, ua.getPharmaWorkQueue().getDispensaryList());
        account.getPharmaWorkQueue().removeDispensaryRequest(req);
                
        Map<String,Date> reqMap = req.getStatusMap();
        reqMap.put(account+" Update:"+txtMsg.getText(), new Date());
        reqMap.put("Request Completed by-"+account, new Date());
        req.setStatusMap(reqMap);
        //    pharmaRequest.setSender(account);
        account.getPharmaWorkQueue().removeDispensaryRequest(req);
        business.getPharmaQueue().updateDispensaryRequest(req, business.getPharmaQueue().getDispensaryList());
        JOptionPane.showMessageDialog(null, "Medicine Delivered and Message Submitted!", "Information", JOptionPane.INFORMATION_MESSAGE);
        populatePatientRequests();
    }//GEN-LAST:event_btnProcessActionPerformed



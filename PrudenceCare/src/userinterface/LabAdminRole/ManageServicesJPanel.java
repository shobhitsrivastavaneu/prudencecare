    private void btnServicesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnServicesActionPerformed
        // TODO add your handling code here:
        if(txtService.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Service Name is mandatory!", "Warning", JOptionPane.WARNING_MESSAGE);       
                  return;         
        }
        LabEnterprise e = (LabEnterprise) enterprise;
        for(String s: e.getServices()){
            if(s.equals(txtService.getText())){
       JOptionPane.showMessageDialog(null, "Service Name is already present!", "Warning", JOptionPane.WARNING_MESSAGE);       
                  return;      
            }
        }
        List<String> sList = e.getServices();
        sList.add(txtService.getText());
        e.setServices(sList);
       JOptionPane.showMessageDialog(null, "Service Name added successfully!", "Information", JOptionPane.INFORMATION_MESSAGE);       
      populateServices();
      txtService.setText("");
    }//GEN-LAST:event_btnServicesActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
                int selectedRow = serviceTable.getSelectedRow();
        if(selectedRow <= 0){
                     JOptionPane.showMessageDialog(null, "Please select a service from table!", "Warning", JOptionPane.WARNING_MESSAGE);       
                  return;
        }

        String s = (String) serviceTable.getValueAt(selectedRow, 0);
                LabEnterprise e = (LabEnterprise) enterprise;
                List<String> sList = e.getServices();
                sList.remove(s);
        e.setServices(sList);
         JOptionPane.showMessageDialog(null, "Service Name removed successfully!", "Information", JOptionPane.INFORMATION_MESSAGE);       
      populateServices();
    }//GEN-LAST:event_btnRemoveActionPerformed


